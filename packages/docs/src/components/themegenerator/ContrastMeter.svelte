<script>
  import { wcagContrast } from "culori"
  let { color1, color2 } = $props()
  function contrastRating(color1, color2) {
    const contrast = wcagContrast(color1, color2)
    if (contrast >= 4.5) {
      return "great"
    }
    if (contrast >= 3) {
      return "good"
    }
    if (contrast >= 2) {
      return "low"
    }
    return "bad"
  }
</script>

